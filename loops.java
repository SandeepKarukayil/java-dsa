public class loops {
    public static void main(String[] args) {
        // while
        int n = 10;
        int i = 1;
        
        while (i <= n) {
            System.out.println(i);
            i++;
        }

        //for 
        for (int j = 1; j <= 20; j++) {
            System.out.println( j);
        }
    }
    
}
